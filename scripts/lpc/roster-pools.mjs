/** Personalidade por bot-N (debe coincidir con citizen-personalities.js). */
export const BOT_PERSONALITY_IDS = [
  'peregrino',
  'comerciante',
  'abuela',
  'estudiante',
  'artesao',
  'jubilado',
  'turista',
  'profesor',
  'retrancero',
  'maior',
  'hosteleira',
  'guia',
  'mariscador',
  'gaiteiro',
  'ciclista',
  'foraneiro',
];

const BOT_TINTS = [0xffffff, 0xfff8f0, 0xf0f8ff, 0xfff0f5, 0xf5fff5, 0xfffff0];

function torsoForBot(personalityId, female, variant) {
  if (personalityId === 'profesor' || personalityId === 'guia') {
    return female ? TORSO.formalF : TORSO.formalM;
  }
  if (personalityId === 'comerciante' || personalityId === 'hosteleira' || personalityId === 'mariscador') {
    return female ? TORSO.buttonedF : TORSO.vneckM;
  }
  if (personalityId === 'peregrino' || personalityId === 'ciclista' || personalityId === 'estudiante') {
    return female ? TORSO.scoopF : TORSO.tshirtM;
  }
  if (personalityId === 'artesao' || personalityId === 'gaiteiro') {
    return female ? TORSO.tshirtF : TORSO.tshirtM;
  }
  const pool = female
    ? [TORSO.tshirtF, TORSO.scoopF, TORSO.buttonedF, TORSO.formalF]
    : [TORSO.tshirtM, TORSO.vneckM, TORSO.formalM];
  return pool[variant % pool.length];
}

/**
 * Perfil visual + personalidade para cidadáns (bot-0…103).
 * @returns {{ layers: string[], personalityId: string, label: string, tint?: number }}
 */
export function botProfile(variant = 0) {
  const personalityId = BOT_PERSONALITY_IDS[variant % BOT_PERSONALITY_IDS.length];
  const female =
    personalityId === 'hosteleira' ||
    personalityId === 'abuela' ||
    (variant % 3 === 0 && personalityId !== 'retrancero');
  const teen = personalityId === 'estudiante' && variant % 2 === 0;
  const bodyKey =
    personalityId === 'mariscador' || personalityId === 'ciclista'
      ? 'muscular'
      : teen
        ? 'teen'
        : female
          ? 'female'
          : 'male';

  const layers = [BODIES[bodyKey]];
  layers.push(female || teen ? LEGS.thinPants : LEGS.malePants);
  layers.push(
    personalityId === 'peregrino' || personalityId === 'ciclista'
      ? FEET.maleBoots
      : female || teen
        ? FEET.thinShoes
        : variant % 4 === 0
          ? FEET.maleBoots
          : FEET.maleShoes
  );
  layers.push(torsoForBot(personalityId, female || teen, variant));

  const fantasyEligible = new Set(['turista', 'foraneiro', 'retrancero', 'gaiteiro', 'maior']);
  const fantasy =
    fantasyEligible.has(personalityId) &&
    ((personalityId === 'turista' && variant % 3 === 0) ||
      (personalityId === 'foraneiro' && variant % 2 === 1) ||
      (personalityId === 'retrancero' && variant % 5 === 0) ||
      (personalityId === 'gaiteiro' && variant % 4 === 0) ||
      (personalityId === 'maior' && variant % 6 === 0));

  if (fantasy) {
    layers.push(FANTASY_HEADS[Math.floor(variant / 11) % FANTASY_HEADS.length]);
  } else {
    layers.push(teen ? HEADS.maleSmall : female ? HEADS.female : HEADS.male);
    const useHat =
      personalityId === 'guia' ||
      personalityId === 'hosteleira' ||
      personalityId === 'gaiteiro' ||
      personalityId === 'jubilado' ||
      variant % 6 === 2;
    const hairList = useHat ? HAT_HAIR : HAIR;
    layers.push(hairList[(variant * 17 + personalityId.length) % hairList.length]);
  }

  const labels = {
    peregrino: 'Peregrino',
    comerciante: 'Comerciante',
    abuela: 'Veciña',
    estudiante: 'Estudiante',
    artesao: 'Artesán',
    jubilado: 'Xubilado',
    turista: 'Visitante',
    profesor: 'Profesor',
    retrancero: 'Retrancero',
    maior: 'Maior',
    hosteleira: 'Hosteleira',
    guia: 'Guía',
    mariscador: 'Mariscador',
    gaiteiro: 'Gaiteiro',
    ciclista: 'Ciclista',
    foraneiro: 'Foraneiro',
  };

  return {
    layers,
    personalityId,
    label: labels[personalityId] || `Cidadán ${variant}`,
    tint: BOT_TINTS[variant % BOT_TINTS.length],
  };
}

export function botLayers(variant = 0) {
  return botProfile(variant).layers;
}

export function comboForBody(bodyKey, hairIdx, variant = 0) {
  const female = bodyKey === 'female' || (bodyKey === 'teen' && variant % 2 === 1);
  const teen = bodyKey === 'teen';
  const body = BODIES[bodyKey] || BODIES.male;
  const head = teen ? HEADS.maleSmall : female ? HEADS.female : HEADS.male;
  const legs = female ? LEGS.thinPants : LEGS.malePants;
  const feet = female ? FEET.thinShoes : FEET.maleShoes;
  const torso = female
    ? [TORSO.tshirtF, TORSO.scoopF, TORSO.buttonedF, TORSO.formalF][variant % 4]
    : [TORSO.tshirtM, TORSO.vneckM, TORSO.formalM][variant % 3];
  const hair = HAIR[hairIdx % HAIR.length];
  return [body, legs, feet, torso, head, hair];
}

/** Esqueleto LPC: corpo + (túnica/belt opcional) + cabeza calavera. */
export function skeletonLayers(variant = 0) {
  const layers = [BODIES.skeleton];
  const robeSlot = variant % (SKELETON_ROBES.length + 1);
  if (robeSlot > 0) layers.push(SKELETON_ROBES[robeSlot - 1]);
  if (variant % 5 === 4) {
    layers.push(SKELETON_BELTS[Math.floor(variant / 5) % SKELETON_BELTS.length]);
  }
  layers.push(variant % 3 === 0 ? HEADS.skeletonAdult : HEADS.skeleton);
  return layers;
}

export function specterLayers(variant = 0) {
  const female = variant % 2 === 0;
  const body = variant % 3 === 0 ? BODIES.zombie : female ? BODIES.female : BODIES.male;
  const head = female ? HEADS.female : HEADS.male;
  const legs = female ? LEGS.thinLeggings : LEGS.maleLeggings;
  const feet = female ? FEET.thinShoes : FEET.maleShoes;
  const torso = female ? TORSO.scoopF : TORSO.formalM;
  const hair = HAIR[(variant * 11 + 13) % HAIR.length];
  return [body, legs, feet, torso, head, hair];
}
